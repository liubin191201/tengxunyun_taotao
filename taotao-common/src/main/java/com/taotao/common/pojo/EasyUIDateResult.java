package com.taotao.common.pojo;

import java.util.List;

/**
 * 
 * 描述：将页面请求的结果封装返回
 * @author fengruiqi
 * @time  2017年1月26日 下午5:06:14
 */
public class EasyUIDateResult {
	/**
	 * 总条数
	 */
	private long total;
	/**
	 * 查询到的数据
	 */
	private List<?> rows;
	
	public EasyUIDateResult(long total, List<?> rows) {
		this.total = total;
		this.rows = rows;
	}
	
	public long getTotal() {
		return total;
	}
	public void setTotal(long total) {
		this.total = total;
	}
	public List<?> getRows() {
		return rows;
	}
	public void setRows(List<?> rows) {
		this.rows = rows;
	}
	
}
